package Swapping;

public class swappingNumber {
    public static void main(String[] args) {

        int num1=100;
        int num2=500;

        System.out.println("Number one is num1="+num1);
        System.out.println("Number two is num2="+num2);

        int x=num1;
        num1=num2;
        num2=x;


        System.out.println("After swapping First number num1="+num1);
        System.out.println("After swapping second number num2="+num2);




    }
}
